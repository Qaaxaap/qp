/* Run a MAKEFS build: prepare, build, package.
   Returns the stage directory path. */
static std::string
run_makefs_build (const qp::MakefsSpec &spec, const Config &cfg)
{
  const std::string &name = spec.name;
  std::string stage = cfg.build_dir + "/stage/" + name;
  std::string build = cfg.build_dir + "/build/" + name;
  std::string srcdir = cfg.build_dir + "/source/" + name;

  /* Create build directories. */
  run_command ({ "mkdir", "-p", stage });
  run_command ({ "mkdir", "-p", build });
  run_command ({ "mkdir", "-p", srcdir });

  /* Build a wrapper script that defines MAKEFS variables and
     functions, then calls prepare + build + package. */
  std::string script_path = cfg.build_dir + "/_build_" + name + ".sh";
  std::ofstream script (script_path);
  if (!script)
    {
      std::printf (_ ("error: cannot write build script\n"));
      return "";
    }
  script << "#!/bin/bash\n";
  script << "set -e\n";
  script << "export STAGE='" << stage << "'\n";
  script << "export BUILD_DIR='" << build << "'\n";
  script << "export SRCDIR='" << srcdir << "'\n";
  script << "mkdir -p \"$STAGE\" \"$BUILD_DIR\" \"$SRCDIR\"\n";
  script << "cd \"$SRCDIR\"\n";

  /* Emit MAKEFS metadata as bash variables (no spaces around =). */
  script << "NAME='" << spec.name << "'\n";
  script << "VERSION='" << spec.version << "'\n";
  script << "RELEASE='" << spec.release << "'\n";

  /* Expand $(VAR) references in a string. */
  auto expand_vars = [&spec] (std::string body) -> std::string
    {
      auto subst = [&body] (const std::string &token, const std::string &value)
        {
          size_t pos = 0;
          while ((pos = body.find (token, pos)) != std::string::npos)
            {
              body.replace (pos, token.size (), value);
              pos += value.size ();
            }
        };
      subst ("$(NAME)", spec.name);
      subst ("$(VERSION)", spec.version);
      subst ("$(RELEASE)", spec.release);
      return body;
    };

  /* Emit functions as proper bash function definitions. */
  for (const auto &[fname, fbody] : spec.functions)
    {
      script << fname << "() {\n";
      script << expand_vars (fbody) << "\n";
      script << "}\n";
    }

  /* Pre-install hook. */
  if (!spec.pre_install.empty ())
    {
      script << spec.pre_install << "\n";
    }

  script << "cd \"$SRCDIR\" && prepare\n";
  script << "cd \"$SRCDIR\" && build\n";
  script << "cd \"$SRCDIR\" && package\n";
  script.close ();

  run_command ({ "chmod", "+x", script_path });

  std::printf (_ ("Building %s...\n"), name.c_str ());
  int rc = run_command ({ "bash", script_path });
  if (rc != 0)
    {
      std::printf (_ ("error: build failed for %s (exit %d)\n"), name.c_str (),
                   rc);
      return "";
    }

  return stage;
}

/* Install a single package: parse MAKEFS, build, register in db.
   The 'installing' set tracks the current call chain to detect cycles.
   Returns 0 on success. */
static int
install_package (const Config &cfg, lfspkg::PackageDB &db,
                 const std::string &name, std::set<std::string> &installing)
{
  if (db.installed (name))
    {
      std::printf (_ ("%s: already installed.\n"), name.c_str ());
      return 0;
    }

  std::string makefs_path = cfg.repo_dir + "/packages/" + name + "/MAKEFS";

  qp::MakefsSpec spec;
  std::string err;
  if (!qp::parse_makefs (makefs_path, spec, err))
    {
      std::printf (_ ("error: %s\n"), err.c_str ());
      return 1;
    }

  /* Mark this package as being installed (for cycle detection). */
  installing.insert (name);

  /* Install runtime dependencies first. */
  for (const auto &dep : spec.depends)
    {
      if (!db.installed (dep))
        {
          if (installing.count (dep))
            {
              std::printf (_ ("warning: cycle detected: %s <-> %s\n"),
                           name.c_str (), dep.c_str ());
              continue;
            }
          std::printf (_ ("Installing dependency: %s\n"), dep.c_str ());
          int rc = install_package (cfg, db, dep, installing);
          if (rc != 0)
            {
              installing.erase (name);
              return rc;
            }
        }
    }

  installing.erase (name);

  /* Build the package. */
  std::string stage = run_makefs_build (spec, cfg);
  if (stage.empty ())
    return 1;

  /* Register in lfspkg. */
  lfspkg::PackageSpec pkg_spec;
  pkg_spec.name = spec.name;
  pkg_spec.version = spec.version;
  pkg_spec.stage_dir = stage;
  pkg_spec.deps = spec.depends;

  try
    {
      lfspkg::apply_install_or_upgrade (db, pkg_spec, false);
    }
  catch (const std::exception &e)
    {
      std::printf ("%s: %s\n", _ ("install error"), e.what ());
      return 1;
    }

  std::printf (_ ("Installed %s %s\n"), spec.name.c_str (),
               spec.version.c_str ());
  return 0;
}

/* Rebuild a package (already installed). */
static int
rebuild_package (const Config &cfg, lfspkg::PackageDB &db,
                 const std::string &name)
{
  if (!db.installed (name))
    {
      std::printf (_ ("%s: not installed. Use 'qp install'.\n"),
                   name.c_str ());
      return 1;
    }

  std::string makefs_path = cfg.repo_dir + "/packages/" + name + "/MAKEFS";

  qp::MakefsSpec spec;
  std::string err;
  if (!qp::parse_makefs (makefs_path, spec, err))
    {
      std::printf (_ ("error: %s\n"), err.c_str ());
      return 1;
    }

  std::string stage = run_makefs_build (spec, cfg);
  if (stage.empty ())
    return 1;

  lfspkg::PackageSpec pkg_spec;
  pkg_spec.name = spec.name;
  pkg_spec.version = spec.version;
  pkg_spec.stage_dir = stage;
  pkg_spec.deps = spec.depends;

  try
    {
      lfspkg::apply_install_or_upgrade (db, pkg_spec, true);
    }
  catch (const std::exception &e)
    {
      std::printf ("%s: %s\n", _ ("upgrade error"), e.what ());
      return 1;
    }

  std::printf (_ ("Rebuilt %s %s\n"), spec.name.c_str (),
               spec.version.c_str ());
  return 0;
}

/* Sort packages so dependencies come first. */
static std::vector<std::string>
topo_sort (lfspkg::PackageDB &db, const std::vector<std::string> &pkgs)
{
  std::set<std::string> pkg_set (pkgs.begin (), pkgs.end ());
  std::vector<std::string> sorted;
  std::set<std::string> visited;

  for (const auto &p : pkgs)
    {
      /* Walk dep chain of p, emitting deps before p. */
      std::vector<std::string> stack;
      std::set<std::string> in_stack;

      auto push = [&] (auto &&self, const std::string &cur) -> bool
        {
          if (visited.count (cur))
            return true;
          if (in_stack.count (cur))
            {
              std::printf (_ ("warning: cycle detected, skipping %s\n"),
                           cur.c_str ());
              return true; /* cycle — skip to avoid infinite loop */
            }
          if (!pkg_set.count (cur))
            {
              visited.insert (cur);
              return true;
            }
          in_stack.insert (cur);

          try
            {
              auto meta = db.read_meta (cur);
              for (const auto &dep : meta.deps)
                {
                  if (!self (self, dep))
                    return false;
                }
            }
          catch (...)
            {
            }

          in_stack.erase (cur);
          visited.insert (cur);
          sorted.push_back (cur);
          return true;
        };
      push (push, p);
    }

  return sorted;
}

/* Read repo index, return name→version map. */
static std::map<std::string, std::string>
load_index_versions (const Config &cfg)
{
  std::map<std::string, std::string> vers;
  std::ifstream in (cfg.repo_dir + "/meta/index");
  if (!in)
    return vers;
  std::string line;
  while (std::getline (in, line))
    {
      if (line.empty () || line[0] == '#')
        continue;
      std::istringstream ss (line);
      std::vector<std::string> fields;
      std::string f;
      while (std::getline (ss, f, '\t'))
        fields.push_back (f);
      if (fields.size () >= 3)
        vers[fields[1]] = fields[2];
    }
  return vers;
}

/* Rebuild a set of packages, cascading to newly-stale packages.
   The 'rebuilt' set prevents re-rebuilding the same package within
   one cascade chain (guards against cyclic built_deps oscillation). */
static int
rebuild_cascade (const Config &cfg, lfspkg::PackageDB &db,
                 const std::vector<std::string> &initial,
                 std::set<std::string> &rebuilt)
{
  if (initial.empty ())
    {
      std::printf (_ ("Nothing to rebuild.\n"));
      return 0;
    }

  /* Filter out packages already rebuilt in this cascade. */
  std::vector<std::string> to_build;
  for (const auto &pkg : initial)
    {
      if (rebuilt.count (pkg))
        continue;
      to_build.push_back (pkg);
    }

  if (to_build.empty ())
    return 0;

  std::vector<std::string> ordered = topo_sort (db, to_build);

  for (const auto &pkg : ordered)
    {
      int rc = rebuild_package (cfg, db, pkg);
      if (rc != 0)
        return rc;
      rebuilt.insert (pkg);
    }

  /* Check for newly-stale packages (cascade). */
  auto new_stale = lfspkg::find_stale_packages (db);
  if (!new_stale.empty ())
    {
      std::printf (_ ("%d package(s) now stale, rebuilding...\n"),
                   static_cast<int> (new_stale.size ()));
      return rebuild_cascade (cfg, db, new_stale, rebuilt);
    }

  return 0;
}

