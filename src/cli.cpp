int
cmd_update (int argc, char **argv)
{
  (void)argc;
  (void)argv;

  Config cfg = load_config ();

  /* Create parent directory. */
  size_t slash = cfg.repo_dir.rfind ('/');
  if (slash != std::string::npos)
    run_command ({ "mkdir", "-p", cfg.repo_dir.substr (0, slash) });

  struct stat st;
  if (stat (cfg.repo_dir.c_str (), &st) != 0)
    {
      std::printf (_ ("Cloning %s...\n"), cfg.repo_url.c_str ());
      int rc = run_command ({ "git", "clone", cfg.repo_url, cfg.repo_dir });
      if (rc != 0)
        {
          std::printf (_ ("error: git clone failed\n"));
          return 1;
        }
    }
  else
    {
      std::string dotgit = cfg.repo_dir + "/.git";
      if (stat (dotgit.c_str (), &st) == 0)
        {
          std::printf (_ ("Updating %s...\n"), cfg.repo_dir.c_str ());
          int rc = run_command (
              { "git", "-C", cfg.repo_dir, "pull", "--ff-only" });
          if (rc != 0)
            {
              std::printf (_ ("error: git pull failed\n"));
              return 1;
            }
        }
    }

  std::string err;
  int count = generate_index (cfg.repo_dir, err);
  if (count < 0)
    {
      std::printf ("%s\n", err.c_str ());
      return 1;
    }

  std::printf (_ ("Indexed %d package(s).\n"), count);
  return 0;
}

int
cmd_search (int argc, char **argv)
{
  if (argc < 2)
    {
      std::printf (_ ("Usage: qp search <keyword>\n"));
      return 1;
    }

  Config cfg = load_config ();
  std::string keyword = argv[1];

  std::ifstream in (cfg.repo_dir + "/meta/index");
  if (!in)
    {
      std::printf (_ ("No package index found. Run 'qp update' first.\n"));
      return 1;
    }

  bool found = false;
  std::string line;
  while (std::getline (in, line))
    {
      if (line.empty () || line[0] == '#')
        continue;

      std::vector<std::string> fields;
      std::istringstream ss (line);
      std::string field;
      while (std::getline (ss, field, '\t'))
        fields.push_back (field);

      if (fields.size () < 4)
        continue;

      std::string name_low = fields[1];
      std::string desc_low = fields[3];
      for (auto &c : name_low)
        c = static_cast<char> (std::tolower (static_cast<unsigned char> (c)));
      for (auto &c : desc_low)
        c = static_cast<char> (std::tolower (static_cast<unsigned char> (c)));

      std::string kw_low = keyword;
      for (auto &c : kw_low)
        c = static_cast<char> (std::tolower (static_cast<unsigned char> (c)));

      if (name_low.find (kw_low) != std::string::npos
          || desc_low.find (kw_low) != std::string::npos)
        {
          found = true;
          if (fields[0] == fields[1])
            std::printf ("%-28s %-10s %s\n", fields[1].c_str (),
                         fields[2].c_str (), fields[3].c_str ());
          else
            std::printf ("%-28s %-10s %s (%s %s)\n", fields[1].c_str (),
                         fields[2].c_str (), fields[3].c_str (), _ ("from"),
                         fields[0].c_str ());
        }
    }

  if (!found)
    std::printf (_ ("No packages found matching '%s'.\n"), keyword.c_str ());

  return 0;
}

