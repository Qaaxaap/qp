int
cmd_install (int argc, char **argv)
{
  if (argc < 2)
    {
      std::printf (_ ("Usage: qp install <packages...>\n"));
      return 1;
    }

  Config cfg = load_config ();
  auto db = make_db (cfg);

  /* Ensure repo is available. */
  struct stat st;
  if (stat (cfg.repo_dir.c_str (), &st) != 0)
    cmd_update (0, nullptr);

  std::set<std::string> installing;
  int rc = 0;
  for (int i = 1; i < argc; ++i)
    {
      rc = install_package (cfg, db, argv[i], installing);
      if (rc != 0)
        break;
    }

  /* Cascade: rebuild anything made stale by this install. */
  if (rc == 0)
    {
      auto stale = lfspkg::find_stale_packages (db);
      if (!stale.empty ())
        {
          std::printf (_ ("%d package(s) stale, rebuilding...\n"),
                       static_cast<int> (stale.size ()));
          std::set<std::string> rebuilt;
          rc = rebuild_cascade (cfg, db, stale, rebuilt);
        }
    }

  return rc;
}

int
cmd_rebuild (int argc, char **argv)
{
  Config cfg = load_config ();
  auto db = make_db (cfg);

  /* Ensure repo is available. */
  struct stat st;
  if (stat (cfg.repo_dir.c_str (), &st) != 0)
    cmd_update (0, nullptr);

  std::set<std::string> rebuilt;

  if (argc < 2 || std::strcmp (argv[1], "--stale") == 0)
    {
      auto stale = lfspkg::find_stale_packages (db);
      if (stale.empty ())
        {
          std::printf (_ ("No stale packages found.\n"));
          return 0;
        }
      std::printf (_ ("Rebuilding %d stale package(s)...\n"),
                   static_cast<int> (stale.size ()));
      return rebuild_cascade (cfg, db, stale, rebuilt);
    }

  if (std::strcmp (argv[1], "--all") == 0)
    {
      auto all = db.list_packages ();
      if (all.empty ())
        {
          std::printf (_ ("No packages installed.\n"));
          return 0;
        }
      std::printf (_ ("Rebuilding all %d package(s)...\n"),
                   static_cast<int> (all.size ()));
      return rebuild_cascade (cfg, db, all, rebuilt);
    }

  /* Rebuild specific packages. */
  int rc = 0;
  for (int i = 1; i < argc; ++i)
    {
      rc = rebuild_package (cfg, db, argv[i]);
      if (rc != 0)
        break;
      rebuilt.insert (argv[i]);
    }

  /* Cascade after explicit rebuilds. */
  if (rc == 0)
    {
      auto stale = lfspkg::find_stale_packages (db);
      if (!stale.empty ())
        {
          std::printf (_ ("%d package(s) now stale, rebuilding...\n"),
                       static_cast<int> (stale.size ()));
          rc = rebuild_cascade (cfg, db, stale, rebuilt);
        }
    }

  return rc;
}

int
cmd_upgrade (int argc, char **argv)
{
  Config cfg = load_config ();
  auto db = make_db (cfg);

  /* Ensure repo is available. */
  struct stat st;
  if (stat (cfg.repo_dir.c_str (), &st) != 0)
    cmd_update (0, nullptr);

  auto index_vers = load_index_versions (cfg);
  if (index_vers.empty ())
    {
      std::printf (_ ("No package index. Run 'qp update' first.\n"));
      return 1;
    }

  std::vector<std::string> to_upgrade;

  if (argc < 2)
    {
      /* No args: find all packages with newer versions. */
      for (const auto &pkg : db.list_packages ())
        {
          auto it = index_vers.find (pkg);
          if (it == index_vers.end ())
            continue;
          std::string installed;
          try
            {
              installed = db.read_meta (pkg).version;
            }
          catch (...)
            {
              continue;
            }
          if (installed != it->second)
            {
              std::printf (_ ("%s: %s -> %s\n"), pkg.c_str (),
                           installed.c_str (), it->second.c_str ());
              to_upgrade.push_back (pkg);
            }
        }
    }
  else
    {
      /* Specific packages. */
      for (int i = 1; i < argc; ++i)
        {
          const char *pkg = argv[i];
          if (!db.installed (pkg))
            {
              std::printf (_ ("%s: not installed.\n"), pkg);
              return 1;
            }
          auto it = index_vers.find (pkg);
          if (it == index_vers.end ())
            {
              std::printf (_ ("%s: not found in repository.\n"), pkg);
              return 1;
            }
          std::string installed = db.read_meta (pkg).version;
          if (installed == it->second)
            {
              std::printf (_ ("%s: already up to date (%s).\n"), pkg,
                           installed.c_str ());
              continue;
            }
          std::printf (_ ("%s: %s -> %s\n"), pkg, installed.c_str (),
                       it->second.c_str ());
          to_upgrade.push_back (pkg);
        }
    }

  if (to_upgrade.empty ())
    {
      std::printf (_ ("All packages up to date.\n"));
      return 0;
    }

  std::printf (_ ("Upgrading %d package(s)...\n"),
               static_cast<int> (to_upgrade.size ()));
  std::set<std::string> rebuilt;
  return rebuild_cascade (cfg, db, to_upgrade, rebuilt);
}

int
cmd_list (int, char **)
{
  auto db = make_db (load_config ());

  auto pkgs = db.list_packages ();
  if (pkgs.empty ())
    {
      std::printf (_ ("No packages installed.\n"));
      return 0;
    }

  for (const auto &pkg : pkgs)
    {
      try
        {
          auto meta = db.read_meta (pkg);
          std::printf ("%-20s %s\n", meta.name.c_str (),
                       meta.version.c_str ());
        }
      catch (...)
        {
          std::printf ("%-20s (error reading meta)\n", pkg.c_str ());
        }
    }

  return 0;
}

int
cmd_remove (int argc, char **argv)
{
  if (argc < 2)
    {
      std::printf (_ ("Usage: qp remove <packages...>\n"));
      return 1;
    }

  Config cfg = load_config ();
  auto db = make_db (cfg);
  auto owners = db.load_owners ();
  fs::path targetRoot = lfspkg::default_target_root ();

  int rc = 0;
  for (int i = 1; i < argc; ++i)
    {
      const char *pkg = argv[i];
      if (!db.installed (pkg))
        {
          std::printf (_ ("%s: not installed.\n"), pkg);
          rc = 1;
          continue;
        }

      auto revdeps = lfspkg::reverse_dependencies (db, pkg);
      if (!revdeps.empty ())
        {
          std::printf (_ ("%s: required by:"), pkg);
          for (const auto &d : revdeps)
            std::printf (" %s", d.c_str ());
          std::printf ("\n");
          std::printf (_ ("Use 'qp remove' again to force-remove anyway.\n"));
          rc = 1;
          continue;
        }

      lfspkg::remove_package_files (db, pkg, targetRoot, owners);
      std::printf (_ ("Removed %s\n"), pkg);
    }

  db.save_owners_atomic (owners);
  return rc;
}

int
cmd_register (int argc, char **argv)
{
  if (argc < 2)
    {
      std::printf (_ ("Usage: qp register <pkg> [version] [deps...]\n"));
      return 1;
    }

  Config cfg = load_config ();
  auto db = make_db (cfg);

  const char *name = argv[1];
  const char *version = argc >= 3 ? argv[2] : "0";

  if (db.installed (name))
    {
      std::printf (_ ("%s: already registered.\n"), name);
      return 1;
    }

  std::vector<std::string> deps;
  for (int i = 3; i < argc; ++i)
    deps.push_back (argv[i]);

  lfspkg::PackageMeta meta;
  meta.name = name;
  meta.version = version;
  meta.install_time = std::to_string (std::time (nullptr));
  meta.stage_dir = "";
  meta.deps = deps;

  db.ensure ();
  db.write_meta_atomic (meta);
  db.write_manifest_atomic (name, {});
  std::printf (_ ("Registered %s %s\n"), name, version);
  return 0;
}

int
cmd_unregister (int argc, char **argv)
{
  if (argc < 2)
    {
      std::printf (_ ("Usage: qp unregister <packages...>\n"));
      return 1;
    }

  Config cfg = load_config ();
  auto db = make_db (cfg);

  int rc = 0;
  for (int i = 1; i < argc; ++i)
    {
      const char *pkg = argv[i];
      if (!db.installed (pkg))
        {
          std::printf (_ ("%s: not registered.\n"), pkg);
          rc = 1;
          continue;
        }

      /* Remove owned paths from owners. */
      auto owners = db.load_owners ();
      try
        {
          auto manifest = db.read_manifest (pkg);
          bool changed = false;
          for (const auto &e : manifest)
            {
              if (e.type != 'D' && owners.count (e.installPath)
                  && owners[e.installPath] == pkg)
                {
                  owners.erase (e.installPath);
                  changed = true;
                }
            }
          if (changed)
            db.save_owners_atomic (owners);
        }
      catch (...)
        {
        }

      std::error_code ec1, ec2;
      fs::remove (db.meta_path (pkg), ec1);
      fs::remove (db.manifest_path (pkg), ec2);
      std::printf (_ ("Unregistered %s\n"), pkg);
    }

  return rc;
}

const Subcommand *
find_subcommand (const char *name)
{
  for (const Subcommand *sc = subcommands; sc->name != nullptr; ++sc)
    {
      if (std::strcmp (sc->name, name) == 0)
        return sc;
    }
  return nullptr;
}
