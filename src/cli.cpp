static int
generate_index (const std::string &repo_dir, std::string &err)
{
  std::string pkg_dir = repo_dir + "/packages";
  std::string meta_dir = repo_dir + "/meta";

  run_command ({ "mkdir", "-p", meta_dir });

  std::string index_path = meta_dir + "/index";
  std::ofstream out (index_path);
  if (!out)
    {
      err = "cannot write " + index_path;
      return -1;
    }

  out << "# qur package index — generated by qp update\n";
  out << "# TAB separated: SOURCE\tNAME\tVERSION\tDESCRIPTION\n";

  int count = 0;

  DIR *dir = opendir (pkg_dir.c_str ());
  if (dir == nullptr)
    return 0;

  struct dirent *ent;
  while ((ent = readdir (dir)) != nullptr)
    {
      if (ent->d_name[0] == '.')
        continue;

      std::string makefs_path = pkg_dir + "/" + ent->d_name + "/MAKEFS";

      struct stat st;
      if (stat (makefs_path.c_str (), &st) != 0)
        continue;

      qp::MakefsSpec spec;
      std::string parse_err;
      if (!qp::parse_makefs (makefs_path, spec, parse_err))
        continue;

      /* Main package. */
      out << spec.name << "\t" << spec.name << "\t" << spec.version << "\t"
          << spec.description << "\n";
      ++count;

      /* Sub-packages. */
      for (const auto &sub : spec.subpackages)
        {
          out << spec.name << "\t" << sub.name << "\t" << spec.version << "\t"
              << sub.description << "\n";
          ++count;
        }
    }
  closedir (dir);
  return count;
}

